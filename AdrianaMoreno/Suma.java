package AdrianaMoreno;


public class Suma {

    public float realizarSuma(int num1, int num2, double num3, double num4, double num5) {
        float resultado = num1 + num2 + Float.parseFloat(num3 + "") + Float.parseFloat(num4 + "") + Float.parseFloat(num5 + "");
        return resultado;
    }

}
